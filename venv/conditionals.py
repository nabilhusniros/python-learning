'''Comparison Operators'''
'''Less than'''
print(1 < 1)
'''Less than or equal to'''
print(1 <= 1)
'''Greater than'''
print(1 > 1)
'''Greater than or equal to'''
print(1 >= 1)
'''Equal to'''
print(1 == 1)
'''Not equal to'''
print(1 != 1)

'''If, else, elif'''
name = input('What is your name? ')
if name == "Nabil":
    print("Hi, {}".format(name))
elif name == "Nadim":
    print('Hello, Baby! {}'.format(name))
else:
    print('Have a nice day!')